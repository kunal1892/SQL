Stored Programs:
1) Stored Procedures
-- stores reusable set of commands
-- has input as well as output parameters
-- can provide multiple outputs
-- can be called using CALL command

2) Functions (UDF: User Defined Functions)
-- stores reusable set of commands
-- executes logic
-- has only input parameters
-- provide only one output
-- a function can be called within any DML/DDL command BY function name